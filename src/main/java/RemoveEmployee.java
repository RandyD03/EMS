    private void removeEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeEmpActionPerformed
        // TODO add your handling code here:
        //Remove employee mentioned in the emp number
        if (empRemove.getText().equals("")) {//No number input
            System.out.println("No employee number added");
            empNullLabel.setVisible(true);
            empErrorLabel.setVisible(false);
            successLabel.setVisible(false);
        } else {
            
            empNullLabel.setVisible(false);
            empErrorLabel.setVisible(false);

            int empRemoveInt = Integer.parseInt(empRemove.getText());
            empRemoved = mainHT.removeFromTable(empRemoveInt);
            successLabel.setVisible(true);

            if (empRemoved == null) {
                System.out.println("Employee cannot be found");
                empErrorLabel.setVisible(true);
                successLabel.setVisible(false);
            } else {
                System.out.println("Employee successfully removed");
            }
        }
    }//GEN-LAST:event_removeEmpActionPerformed

