    private void undoEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_undoEmpActionPerformed
        // TODO add your handling code here:
        //Adds recently deleted employee back to HT
        if(empRemoved != null){
            mainHT.addToTable(empRemoved);
            empRemoved = null;
            System.out.println("Employee Re-added");
        } else {
            System.out.println("Employee unable to be Re-added :(");
        }
    }//GEN-LAST:event_undoEmpActionPerformed
